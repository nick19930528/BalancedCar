/*==============================================================================================*
**函數 : MPU6050_Init
**功能 : 初始化MPU6050
**輸入 : None
**輸出 : None
**使用 : MPU6050_Init()
**==============================================================================================*/
/*==============================================================================================*/
void MPU6050_Init( void )
{
	u8 MPU6050_Init_Data[6] = {
			0x01,	/* MPU6050_PWR_MGMT_1 */
			0x04,	/* MPU6050_SMPLRT_DIV */
			0x02,	/* MPU6050_CONFIG */
			0x18,	/* MPU6050_GYRO_CONFIG */
			0x08	/* MPU6050_ACCEL_CONFIG */
		};
	I2C_DMA_WriteReg(MPU6050_Init_Data,   MPU6050_I2C_ADDR, MPU6050_PWR_MGMT_1, 1);   Delay_1ms(10);
	I2C_DMA_WriteReg(MPU6050_Init_Data+1, MPU6050_I2C_ADDR, MPU6050_SMPLRT_DIV, 1);   Delay_1ms(10);
	I2C_DMA_WriteReg(MPU6050_Init_Data+2, MPU6050_I2C_ADDR, MPU6050_CONFIG, 1);       Delay_1ms(10);
	I2C_DMA_WriteReg(MPU6050_Init_Data+3, MPU6050_I2C_ADDR, MPU6050_GYRO_CONFIG, 1);  Delay_1ms(10);
	I2C_DMA_WriteReg(MPU6050_Init_Data+4, MPU6050_I2C_ADDR, MPU6050_ACCEL_CONFIG, 1); Delay_1ms(10);
}
